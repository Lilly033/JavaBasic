package com.syntax.class08;

import java.util.Scanner;
 
public class ReplitPractice {                               //printing out series of numbers
	//                                                       ending at the double value of end

	public static void main(String[] args) {                 // int end;

		for (int y = 1; y <= 10; ++y) {
		      for (int x = 1; x <= 10; ++x) {
		        if (y>5 ){
		          break;
		        }
		        System.out.print( y*x + " ");
		      }
		      System.out.println();
		    }
		  }
		
		
		
		
		
		
		

}
